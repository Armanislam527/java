//package datatypes;

public class dataTypesCharacter {

    public static void main(String[] args) {
        char c1 = 'A';//directly assigning a character
        c2 = 65;//ASCII encoding for character 'A'
        char c3 = '\u0041';//unicode encoding for character 'A'
        System.out.println("c1: " + c1);
        System.out.println("c2: " + c2);
        System.out.println("c3: " + c3);
    }
}
